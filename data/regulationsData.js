// Create search index for regulations
export const createRegulationSearchIndex = (data) => {
  const index = new Map();

  data.forEach((item, idx) => {
    if (!item.keywords) return;

    const words = item.keywords.toLowerCase().split(/\s+/);

    words.forEach(word => {
      if (word.length < 2) return;

      if (!index.has(word)) {
        index.set(word, []);
      }
      index.get(word).push(idx);
    });
  });

  return index;
};

// Search function with improved relevance scoring
export const searchRegulations = (searchTerm, searchIndex = null) => {
  const data = regulationsData;
  const index = searchIndex || createRegulationSearchIndex(data);

  if (!searchTerm || searchTerm.trim().length === 0) {
    return [];
  }

  const queryWords = searchTerm.toLowerCase().trim().split(/\s+/);
  const resultMap = new Map();

  queryWords.forEach(word => {
    if (index.has(word)) {
      index.get(word).forEach(idx => {
        if (!resultMap.has(idx)) {
          resultMap.set(idx, 0);
        }
        resultMap.set(idx, resultMap.get(idx) + 1);
      });
    }
  });

  // Get unique results sorted by relevance
  const uniqueResults = Array.from(resultMap.entries())
    .map(([idx, score]) => ({ ...data[idx], relevanceScore: score }))
    .filter(item => item && item.id);

  return uniqueResults.sort((a, b) => {
    const aTitle = (a.title || '').toLowerCase();
    const bTitle = (b.title || '').toLowerCase();
    const queryLower = searchTerm.toLowerCase();

    // Prioritize title matches
    if (aTitle.includes(queryLower) && !bTitle.includes(queryLower)) return -1;
    if (!aTitle.includes(queryLower) && bTitle.includes(queryLower)) return 1;

    return 0;
  }).slice(0, 100); // Increased limit for comprehensive results
};